<?php
// 1. ใช้ for วาดรูปดาวเพิ่มทีละแถว
// แถวแรกมี 1 ดวง แถวต่อ ๆ ไปเพิ่มทีละ 1
echo "<h3>ใช้ Loop for</h3>";
for($i = 1; $i <= 4; $i++){          // ลูปจำนวนแถว
    for($j = 1; $j <= $i; $j++){     // ลูปจำนวนดาวในแต่ละแถว
        echo "*";
    }
    echo "<br>";
}

// 2. ใช้ while วาดตัวเลขซ้ำกันในแต่ละแถว
// แต่ละแถวพิมพ์เลข i ซ้ำ 4 ครั้ง
echo "<h3>ใช้ Loop while</h3>";
$i = 1;
while($i <= 3){                      // ลูปแถว 1 ถึง 3
    $j = 1;
    while($j <= 4){                  // ลูปพิมพ์เลขซ้ำ 4 ครั้ง
        echo $i . " ";
        $j++;
    }
    echo "<br>";
    $i++;
}

// 3. ใช้ do while วาดตัวเลขแบบขั้นบันได
// เช่น 1
//      2 2
//      3 3 3
echo "<h3>ใช้ Loop do while</h3>";
$i = 1;
do{
    $j = 1;
    do{
        echo $i . " ";               // พิมพ์เลข i ตามจำนวนแถว
        $j++;
    } while($j <= $i);
    echo "<br>";
    $i++;
} while($i <= 3);

// 4. กรอบล้อมดาวและตัวเลข
// พิมพ์กรอบดาว * รอบนอก
// ด้านในพิมพ์ตัวเลข (แถว - 1)
echo "<h3>กรอบล้อม (ใช้ for)</h3>";
$rows = 5;   // จำนวนแถว
$cols = 6;   // จำนวนคอลัมน์
for($i = 1; $i <= $rows; $i++){
    for($j = 1; $j <= $cols; $j++){
        if($i == 1 || $i == $rows){      // แถวบนสุดและล่างสุด
            echo "* ";
        } else if($j == 1 || $j == $cols){ // คอลัมน์ซ้ายและขวาสุด
            echo "* ";
        } else {
            echo ($i - 1) . " ";         // ภายในกรอบ
        }
    }
    echo "<br>";
}

// 5. ตัวเลขย้อนกลับแบบสามเหลี่ยมกลับหัว
// เช่น 3 3 3
//      2 2
//      1
echo "<h3>ลำดับตัวเลขย้อนกลับ (ใช้ while)</h3>";
$i = 3;
while($i >= 1){
    $j = 1;
    while($j <= $i){
        echo $i . " ";                // พิมพ์เลข i ตามจำนวนครั้งที่ลดลง
        $j++;
    }
    echo "<br>";
    $i--;
}
?>
